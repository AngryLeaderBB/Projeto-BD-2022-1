    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jFormattedTextField1.getText().equals("")||
            jTextField2.getText().equals("")||
            jComboBox1.getSelectedIndex() == -1 ||
            jComboBox2.getSelectedIndex() == -1){
            
            JOptionPane.showMessageDialog(null,"Todos os campos "
                    + "devem ser inseridos", "Erro",
                    JOptionPane.PLAIN_MESSAGE);
        }
        else{
            
            int id = parseInt(jFormattedTextField1.getText());
            String fornecedor = valueOf(jComboBox1.getSelectedItem());
            String equipamento = valueOf(jComboBox2.getSelectedItem());
            
            Equipamento contrato = new Equipamento(id, jTextField2.getText(),
                equipamento, fornecedor);
            equipamentoDao.create(contrato);
            update();
            
        }
        jFormattedTextField1.setText("");
        jTextField2.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

