    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int valorSelecionado = jTable2.getSelectedRow();
        
        if (valorSelecionado != -1){
            
           equipamentoDao.delete(listaEquipamento.get(jTable2.getSelectedRow()));
        }
        
        update();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void update(){
        listaEquipamento = equipamentoDao.findAll();
        
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"ID", "Marca",
        "Fornecedor", "Modelo"},0);
        
        for (int i = 0; i < listaEquipamento.size(); i++){
            Equipamento modeloAux = listaEquipamento.get(i);
            Object linha[] = new Object[] {modeloAux.getId(), modeloAux.getMarca(),
            modeloAux.getFornecedorCnpj(), modeloAux.getModeloEquipamentoNome()};
            
            modelo.addRow(linha);
        }
        
        jTable2.setModel(modelo);
        
        if (listaFornecedor != null){
            listaFornecedor.clear();
        }
        
        if (listaModelo != null){
            listaModelo.clear();
        }
        
        jComboBox1.removeAllItems();
        
        listaFornecedor = forDao.findAll();
        for (int i =0; i < listaFornecedor.size(); i++){
          jComboBox1.addItem(listaFornecedor.get(i).getCnpj());
            }
        
        jComboBox2.removeAllItems();
        
        listaModelo = modDao.findAll();
        for (int i =0; i < listaModelo.size(); i++){
          jComboBox2.addItem(listaModelo.get(i).getNome());
            }
               
    }
    
