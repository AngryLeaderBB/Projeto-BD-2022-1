    private float toFloat(String s){
        return parseFloat(s.replaceAll(",", "."));
    }
    
    private void update(){
        listaAvaliacao = avaDao.findAll();
        
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"Data", 
            "Biceps","Panturilha", "Torax", "Triceps", "Cintura", "Perna", "IMC", 
            "Altura", "Peso", "Peso Magro", "BodyFat", 
            "Professor", "Aluno"},0);
        
        for (int i = 0; i < listaAvaliacao.size(); i++){
            AvaliacaoFisica modeloAux = listaAvaliacao.get(i);
            Object linha[] = new Object[] {modeloAux.getData(), 
                modeloAux.getBiceps(), modeloAux.getPanturilha(),
                modeloAux.getTorax(), modeloAux.getTriceps(),
                modeloAux.getCintura(), modeloAux.getPerna(),
                modeloAux.getImc(), modeloAux.getAltura(), 
                modeloAux.getPeso(), modeloAux.getPesoMagro(),
                modeloAux.getBodyFat(), modeloAux.getProfessorCref(),
                modeloAux.getAlunoCpf()};
            
            modelo.addRow(linha);
        }
        
        jTable2.setModel(modelo);
        
        if (listaProfessor != null){
            listaProfessor.clear();
        }
        
        if (listaAluno != null){
            listaAluno.clear();
        }
        
        jComboBox1.removeAllItems();
        
        listaProfessor = professorDao.findAll();
        for (int i =0; i < listaProfessor.size(); i++){
          jComboBox1.addItem(listaProfessor.get(i).getCref());
            }
        
        jComboBox2.removeAllItems();
        
        listaAluno = alunoDao.findAll();
        for (int i =0; i < listaAluno.size(); i++){
          jComboBox2.addItem(listaAluno.get(i).getCpf());
            }
               
    }
    
