    private String dateTimeToDate(String s){
        StringBuilder res = new StringBuilder();
        //2019-08-89
        res.append(s.substring(8, 10));
        res.append("/");
        res.append(s.substring(5, 7));
        res.append("/");
        res.append(s.substring(0, 4));
        return res.toString();
    }
    
